import random

print(random.randint(5, 20))  # line 1
print(random.randrange(3, 10, 2))  # line 2
print(random.uniform(2.5, 5.5))  # line 3
print(random.randint(1, 100))

# The line 1 smallest number is 5, the largest is 20
# The line 2 smallest number is 3, the largest is 10. the line 2 couldn't have number 4.
# The line 3 smallest number is 2.5, the largest is 5.5
